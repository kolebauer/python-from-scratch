# The print() function sends text or other data to the console (standard output).
# This is one of the most common ways to quickly check program behavior or display results.

# -------------------------------------------------------
# Example 1: Printing a static message
# -------------------------------------------------------
# A "static message" means the content is fixed - it does not change when the program runs.
print("Hello, World!")  # Common first program to verify Python is set up correctly

# -------------------------------------------------------
# Example 2: Printing multiple values
# -------------------------------------------------------
# The print() function can take multiple arguments, separated by commas.
# Python will add a space between each value by default.
print("Hello", "Python", 3.13)

# -------------------------------------------------------
# Example 3: Printing a blank line
# -------------------------------------------------------
# Printing an empty string ("") creates a blank line in the output.
# This is useful for visually separating different sections of output in the terminal.
print("")  # Blank line
print("This text appears after a blank line.")
